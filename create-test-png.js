const fs = require('fs');

// 创建一个简单的 8x8 PNG 图片
const pngData = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,  // PNG 签名
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,  // IHDR 块
    0x00, 0x00, 0x00, 0x08, 0x00, 0x00, 0x00, 0x08,
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x3D,
    0xDE,
    0x00, 0x00, 0x00, 0x09, 0x49, 0x44, 0x41, 0x54,  // IDAT 块
    0x78, 0x9C, 0x63, 0x00, 0x01, 0x00, 0x00, 0x05,
    0x00, 0x01, 0x0D, 0x0A, 0x2D, 0xB4,
    0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44,  // IEND 块
    0xAE, 0x42, 0x60, 0x82
]);

// 写入文件
fs.writeFileSync('public/test-image.png', pngData);
console.log('✅ 已创建测试图片: public/test-image.png');

// 验证文件
const stat = fs.statSync('public/test-image.png');
console.log(`   大小: ${stat.size} 字节`);

// 检查文件类型
try {
    const buffer = fs.readFileSync('public/test-image.png');
    const isPNG = buffer.slice(0, 8).equals(
        Buffer.from([0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A])
    );
    console.log(`   是 PNG 文件: ${isPNG}`);
} catch (err) {
    console.error('❌ 文件读取错误:', err);
}